var numero1 = 10;
var numero2 = 5;

// Suma
var suma = numero1 + numero2;
console.log("Suma:", suma); // Output: Suma: 15

// Resta
var resta = numero1 - numero2;
console.log("Resta:", resta); // Output: Resta: 5

// Multiplicación
var multiplicacion = numero1 * numero2;
console.log("Multiplicación:", multiplicacion); // Output: Multiplicación: 50

// División
var division = numero1 / numero2;
console.log("División:", division); // Output: División: 2

// División entera (cociente)
var divisionEntera = Math.floor(numero1 / numero2);
console.log("División Entera:", divisionEntera); // Output: División Entera: 2
